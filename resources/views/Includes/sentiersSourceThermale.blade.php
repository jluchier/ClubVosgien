    <p>
      Sens conseillé :
    </p>
    <p>Prendre la direction Table d’Orientation et monter tranquillement parmi maisons et chalets accrochés à la colline mais aussi devant une ferme rénovée perdue au milieu des sapins.
    </p>
    <p>
      A la croix des Cucherons, prendre direction « Chalet de l’Avuxon – Source thermale », un chemin goudronné vous mènera vers le Gros Chêne (vieil arbre). Au passage admirez la ferme et ses dépendances mais aussi la magnifique vue sur la vallée de Reherrey et les montagnes environnantes.
    </p>
    <p>Au Gros Chêne, continuer son chemin sous les ombrages et rejoindre la source thermale de Chaudefontaine (l’eau y sort de terre à 21°. Avec le temps la température  a  diminué de quelques degrés en se mélangeant aux eaux du proche ruisseau).
    </p>
    <p>Le chemin descendant vous fera découvrir le hameau de Reherrey, sa ferme bio et pédagogique élevant des vaches de race Vosgienne dans un cadre naturel entouré de forêt et pâturages, hébergement en camping et gîte.
    </p>
    <p>Après la ferme, franchir le pont sur le ruisseau du Ravage et rejoindre le village et point de départ par sentiers et chemins.
    </p>
