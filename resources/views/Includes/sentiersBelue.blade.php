    <p>
      Partir par la rue de Parrier, passer devant l’Eglise et à 250m partir à droite en direction de la forêt.
    </p>
    <p>A nouveau, après 250 m vous arrivez à la boucle <img class="zoom" src="images/common/Balisage_Anneau_Bleu.png"> , sens décrit invite à s’engager à gauche, en direction du chalet de Bélué par la Biolle.
    </p>
    <p>
      En arrivant à la route goudronnée, partir à droite, 50m après les Stèles Urion et Montémont, s’engager à gauche, et 10m avant les maisons, partir à droite sur le sentier qui monte dans la forêt, pour arriver sur un grand chemin de débardage.
    </p>
    <p>S’engager à gauche et continuer ce chemin jusqu’à la Croix du Lait, puis au Chalet de Bélué et sa Table d’Orientation, pour apprécier un joli point de vue sur la vallée de la Haute Moselle.
    </p>
    <p>Continuer le circuit qui vous fera passer par la Croix de Parrier, surplombant le village, pour profiter d’un second point de vue.
    </p>
    <p>Continuer la descente en passant partiellement sur le sentier botanique et arriver à la fin de la boucle.
    </p>
    <p>Reprendre le chemin en sens inverse de l’aller pour rejoindre le point de départ.
    </p>
